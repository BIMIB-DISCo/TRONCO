# Trim spaces in the beginning and in the end of the names in Events variable 
trim.events <- function(events){
	for(i in 1:nrow(events)){
      for(j in 1:(ncol(events) - 1)){
        e <- toString(events[i,j])
        trimmed <- trim(e)
        events[i,j] <- trimmed
      }
    }
    return(events)
}